#  Q.1  Write a program to demonstrate different numeric data types (int, float) and type conversion


# Numeric Data Types
a = 10          # int
b = 5.5         # float

print("Integer value:", a)
print("Float value:", b)

# Type conversion
sum_value = a + b
converted = int(b)

print("Sum (int + float):", sum_value)
print("Float to int conversion:", converted)