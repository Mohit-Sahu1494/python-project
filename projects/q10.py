# Q10. Write a program to perform string operations (slicing, concatenation, reversing).

str1 = "Hello"
str2 = "World"

# Concatenation
result_str = str1 + " " + str2
print("Concatenated String:", result_str)

# Slicing
print("Sliced String (first 5 chars):", result_str[0:5])
print("Sliced String (from index 6):", result_str[6:])

# Reversing
print("Reversed String:", result_str[::-1])
